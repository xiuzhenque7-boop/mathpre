export function getParsedKnowledgePoints(): KnowledgePoint[] {
  const lines = RAW_CSV_DATA.split('\n');
  const result: KnowledgePoint[] = [];
  
  // Skip header
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    
    const parts = line.split(',');
    if (parts.length < 3) continue;
    
    const rawCategory = parts[0].trim();
    const subCategory = parts[1].trim();
    const name = parts[2].trim();
    // Some lines might not have grade
    const gradeRaw = parts[3] ? parts[3].trim() : '';
    
    // Clean grade (e.g. L1上 -> 1年级上, L1-L2 -> 1-2年级, etc.)
    let grade = gradeRaw;
    if (gradeRaw) {
      grade = gradeRaw
        .replace(/^L/, '') // Remove leading L
        .replace('上', '年级上')
        .replace('下', '年级下');
    } else {
      grade = '通用拓展';
    }
    
    // Map raw category to UI categories: '解决问题' mapped to '综合与实践'
    const category = CATEGORY_MAP[rawCategory] || rawCategory;
    
    result.push({
      id: `${category}-${subCategory}-${name}`,
      category,
      subCategory,
      name,
      grade
    });
  }
  
  return result;
}
